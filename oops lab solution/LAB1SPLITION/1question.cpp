#include<iostream>
using namespace std;
int main()
{
    int x, y, answer;
    cout<<"Enter Two Numbers: ";
    cin>>x>>y;
    answer = x+y;
    cout<<endl<<"Addition Result = "<<answer<<endl;
    answer = x-y;
    cout<<endl<<"Subtraction Result = "<<answer<<endl;
    answer = x*y;
    cout<<endl<<"Multiplication Result = "<<answer<<endl;
    
    return 0;
}


